-- ============================================
-- REPORTS & BUSINESS ANALYTICS
-- ============================================

-- WHICH VENDORS SHOULD WE PRIORITIZE PAYING FIRST?
-- SORT BY CREDIT TOLERANCE LEVEL, THEN BY HIGHEST PENDING DEBT
SELECT V.VENDOR_NAME,
       V.TOLERANCE_LEVEL,
       SUM(B.TOTAL_AMOUNT - B.PAID_AMOUNT) AS PENDING_AMOUNT
FROM VENDORS V
JOIN BILLS B ON V.ID = B.VENDOR_ID
WHERE B.STATUS != 'PAID'
GROUP BY V.VENDOR_NAME, V.TOLERANCE_LEVEL
ORDER BY
    CASE
        WHEN V.TOLERANCE_LEVEL = 'LOW' THEN 1
        WHEN V.TOLERANCE_LEVEL = 'MEDIUM' THEN 2
        ELSE 3
    END,
    PENDING_AMOUNT DESC;

-- FIND WHICH USER HAS THE HIGHEST TOTAL PENDING DUE
SELECT U.USER_NAME,
       SUM(B.TOTAL_AMOUNT - B.PAID_AMOUNT) AS TOTAL_PENDING
FROM USERS U
JOIN BILLS B ON U.USER_ID = B.USER_ID
GROUP BY U.USER_NAME
ORDER BY TOTAL_PENDING DESC
LIMIT 1;

-- CALCULATE TOTAL PAYMENT MADE TO EACH VENDOR
SELECT V.VENDOR_NAME,
       SUM(P.AMOUNT) AS TOTAL_RECEIVED
FROM PAYMENTS P
JOIN BILLS B ON P.BILL_ID = B.ID
JOIN VENDORS V ON B.VENDOR_ID = V.ID
GROUP BY V.VENDOR_NAME
ORDER BY TOTAL_RECEIVED DESC;

-- SHOW TOTAL RECEIVED AND PAID AMOUNTS FOR EACH BUSINESS
SELECT USER_ID,
       LOG_TYPE,
       SUM(AMOUNT) AS TOTAL_AMOUNT
FROM DAILY_LOGS
GROUP BY USER_ID, LOG_TYPE
ORDER BY USER_ID;

-- FIND THE VENDOR SUPPLYING THE HIGHEST NUMBER OF DIFFERENT COMMODITIES
SELECT V.VENDOR_NAME,
       COUNT(VC.COMMODITY_ID) AS TOTAL_COMMODITIES
FROM VENDORS V
JOIN VENDOR_COMMODITY VC ON V.ID = VC.VENDOR_ID
GROUP BY V.VENDOR_NAME
ORDER BY TOTAL_COMMODITIES DESC
LIMIT 1;