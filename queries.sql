-- ============================================
-- INVENTORY MANAGEMENT
-- ============================================

-- SHOW ALL INVENTORY ITEMS CURRENTLY TRACKED
SELECT COMMODITY_NAME, QUANTITY, UNIT, MIN_QUANTITY
FROM COMMODITIES
WHERE USER_ID = 1;

-- UPDATE TOTAL STOCK FOR A SPECIFIC COMMODITY
UPDATE COMMODITIES
SET QUANTITY = 75
WHERE ID = 1;

-- FIND INVENTORY ITEMS THAT HAVE NEVER BEEN USED IN ANY BILL
SELECT COMMODITY_NAME
FROM COMMODITIES
WHERE ID NOT IN (
    SELECT COMMODITY_ID
    FROM BILL_COMMODITY
);

-- FIND COMMODITIES BELOW MINIMUM STOCK AND THEIR RECOMMENDED VENDORS
SELECT C.COMMODITY_NAME,
       C.QUANTITY AS CURRENT_STOCK,
       C.MIN_QUANTITY,
       V.VENDOR_NAME AS RECOMMENDED_VENDOR
FROM COMMODITIES C
JOIN VENDOR_COMMODITY VC ON C.ID = VC.COMMODITY_ID
JOIN VENDORS V ON VC.VENDOR_ID = V.ID
WHERE C.QUANTITY < C.MIN_QUANTITY;


