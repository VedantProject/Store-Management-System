CREATE TRIGGER AFTER_BILL_ITEM_INSERT 
AFTER INSERT
ON BILL_COMMODITY
FOR EACH ROW 
BEGIN
	DECLARE V_USER_ID INT;
    
	UPDATE COMMODITIES SET QUANTITY = QUANTITY + NEW.SUPPLIED_AMOUNT WHERE ID = NEW.COMMODITY_ID;
    
    SELECT USER_ID INTO V_USER_ID FROM BILLS WHERE ID=NEW.BILL_ID;
    
    INSERT INTO DAILY_LOGS VALUES (NULL,V_USER_ID,'RECEIVED',NEW.COST,NOW(),CONCAT('Stock Received: ',NEW.COMMODITY_NAME),NOW(),NOW());
END //
DELIMITER ;

DELIMITER //
CREATE TRIGGER AFTER_PAYMENT_INSERT
AFTER INSERT 
ON PAYMENTS 
FOR EACH ROW 
BEGIN 
	DECLARE V_USER_ID INT;
    
	UPDATE BILLS SET PAID_AMOUNT = PAID_AMOUNT + NEW.AMOUNT WHERE ID = NEW.BILL_ID;
    
    UPDATE BILLS 
    SET STATUS = CASE
		WHEN PAID_AMOUNT >= TOTAL_AMOUNT THEN 'PAID'
        WHEN PAID_AMOUNT > 0 AND PAID_AMOUNT < TOTAL_AMOUNT THEN 'PARTIAL'
        ELSE 'UNPAID'
	END
    WHERE ID=NEW.BILL_ID;
    
    SELECT USER_ID INTO V_USER_ID FROM BILLS WHERE ID=NEW.BILL_ID;
    
    INSERT INTO DAILY_LOGS VALUES (NULL,V_USER_ID,'PAID',NEW.AMOUNT,NOW(),'Payment Recorded', NOW(),NOW());
END //
DELIMITER ;

